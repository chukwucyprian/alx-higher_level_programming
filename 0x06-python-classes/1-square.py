#!/usr/bin/python3
"""
    A class representing a square.
"""


class Square:
    """ 
    Attributes:
        __size: Private instance attribute for
        the size of the square.

    """

    def __init__(self, size):
        """
        Initializes a square with the given size.

        Args:
            size: The size of the square.

        """
        self.__size = size
